            //if (dest.ExpertGroup == "Ikke-marine invertebrater")
            //{
            //    dest.ExpertGroup = "Limniske invertebrater";
            //}
            if (((dest.NotApplicableCategory == "canNotEstablishWithin50years" && dest.AlienSpeciesCategory == "NotApplicable") || 
                 (dest.NotApplicableCategory == "traditionalProductionSpecie" && dest.AlienSpeciesCategory == "NotApplicable") || 
                 (dest.DoorKnockerCategory == "noRiskAssessment" && dest.AlienSpeciesCategory == "DoorKnocker")))
            {
                dest.HorizonDoScanning = true;

               
            }
        }


